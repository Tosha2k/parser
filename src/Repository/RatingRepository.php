    public function findTop($date, $category)
    {
	    $query = $this->getEntityManager()->createQuery('
		SELECT r,m
		FROM App\Entity\Rating r
		LEFT JOIN r.movie m
		WHERE r.date = :date
		AND m.category = :category
		ORDER BY r.position ASC ')->setParameter('date', $date->format('Y-m-d'))->setParameter('category',$category)->setMaxResults(10);

	    return $query->getResult();
    }

